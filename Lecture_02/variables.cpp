#include<iostream>
using namespace std;
int main(){
    // Variable declaration
    int age;
    double height;
    char grade;
    string name;

    // Variable initialization
    age = 20;      // age = identifier
    height = 5.9;
    grade = 'A';
    name = "Suyash Mungase";

    // Output the variables
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;

    return 0;
}